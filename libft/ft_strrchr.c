char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*ss;

	i = 0;
	ss = (char *)s;
	while (s[i])
		i++;
	while (i >= 0)
	{
		if (s[i] == c)
			return (ss + i);
		i--;
	}
	return (0);
}
